int readNumber(char* input, int base)
{
 int inputLength = strlen(input);
 if (inputLength == 0)
 {
  return -1; // 输入字符串不对
 }
 
 if (base < 2 || base > 16)
 {
  return -1; // 输入进制不对
 }
 
 int number = 0;
 for (int i = 0; i < inputLength; i++)
 {
  char c = input[i]; // 取一个字符
  int d; // 字符转成数字
  if (c >= '0' && c <= '9')
  {
   d = c - '0';
  }
  else if (c >= 'A' && c <= 'F')
  {
   d = c - 'A' + 10;
  }
  else if (c >= 'a' && c <= 'f')
  {
   d = c - 'a' + 10;
  }
  else
  {
   return -1; // 字符不对
  }
 
  if (d >= base)
  {
   return -1; // 输入字符超过进制允许范围
  }
 
  number *= base;
  number += d;
  if (number < 0)
  {
   return -1; // 输入数字太大
  }
 }
 return number;
}
 
char* writeNumber(int number, int base)
{
 if (base < 2 || base > 16)
 {
  return 0; // 输出进制不对
 }
 
 char buffer[128];
 int index = 127;
 buffer[index--] = 0;
 do
 {
  if (index < 0)
  {
   return 0; // 输出字符串太长
  }
 
  int d = number % base;
  number /= base;
  if (d <= 9)
  {
   buffer[index--] = d + '0';
  }
  else
  {
   buffer[index--] = d - 10 + 'a';
  }
 } while (number != 0);
 
 index++;
 int outputLength = strlen(buffer + index);
 
 char* output = (char*) malloc(outputLength + 1);
 memcpy(output, buffer + index, outputLength + 1);
 return output;
}
 
