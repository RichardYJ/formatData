bool Format16to14(in unsigned short * inArry,out unsigned short *outArry,in unsigned short inLen,out unsigned short *outLen)
{
	int i=0;
	inLen=inLen*16/14;   
	for( int j=0; j<inLen; j++ )
	{
		/*
		if((i%8)==7||(i%8)==0)outArry[i] = inArry[i];
		else outArry[i] = inArry[i-];
		*/
		i=j-j/8;
		int m = j%8;
		switch(m)
		{
		case 0:
			outArry[j] = inArry[i]&0x3fff;
			break;
		case 1:
			outArry[j] = (inArry[i-1]&0xc000)>>14|(inArry[i]&0x0fff)<<2;
			break;
		case 2:
			outArry[j] = (inArry[i-1]&0xf000)>>12|(inArry[i]&0x03ff)<<4;
			break;
		case 3:
			outArry[j] = (inArry[i-1]&0xfc00)>>10|(inArry[i]&0x00ff)<<6;
			break;
		case 4:
			outArry[j] = (inArry[i-1]&0xff00)>>8|(inArry[i]&0x003f)<<8;
			break;
		case 5:
			outArry[j] = (inArry[i-1]&0xffc0)>>6|(inArry[i]&0x000f)<<10;   /////////////////
			break;
		case 6:
			outArry[j] = (inArry[i-1]&0xfff0)>>4|(inArry[i]&0x0003)<<12;
			break;
		case 7:
			outArry[j] = (inArry[i-1]&0xfffc)>>2;			//??????????????????????
			break;
		}
	}
	*outLen = inLen;
	return true;
}

